# ============================================================
# User Prompt: 定义"具体讲什么"
# ============================================================
def build_user_prompt(topic: str, reference_hints: Optional[list[str]] = None) -> str:
    """
    生成 user prompt（V2 强化版）。

    Args:
        topic: 用户输入主题，如"薏米红豆水祛湿"。空字符串/超长会被截断。
        reference_hints: 参考提示（未来可对接图片识别结果）

    Returns:
        user prompt 字符串。
    """
    # 防御：清理输入
    topic = (topic or "").strip()
    if not topic:
        topic = "日常养生分享"  # 兜底主题
    if len(topic) > 200:
        topic = topic[:200]  # 截断

    parts = [
        f"📌 视频主题：「{topic}」",
        "",
        "请按上面定义的JSON格式输出完整脚本，特别注意：",
        "1. 钩子要狠（反常识/纠错/痛点），不要陈词滥调开场",
        "2. 口播按4段结构（0-3s钩子/3-15s痛点/15-30s方案/30-35s收尾）",
        "3. 鞠姐自拍主镜头 + 即梦只做B-roll（不能有人脸）",
        "4. 即梦提示词必须是写实摄影风或电影感纪录片，避审核",
    ]

    if reference_hints:
        hints_text = "、".join(reference_hints[:5])
        parts.append(f"5. 参考要素：{hints_text}")

    parts.append("\n开始写吧，只输出 JSON。")
    return "\n".join(parts)


# ============================================================
# 输出解析辅助
# ============================================================
def strip_markdown_codeblock(text: str) -> str:
    """
    去掉 LLM 输出常见的 ```json...``` 代码块标记。
    系统提示已要求不输出代码块，但LLM有时会无视。
    """
    text = text.strip()
    if text.startswith("```"):
        # 去掉首行 ```json 和尾行 ```
        lines = text.split("\n")
        # 第一行去掉
        lines = lines[1:]
        # 最后一行如果是 ``` 也去掉
        if lines and lines[-1].strip().startswith("```"):
            lines = lines[:-1]
        text = "\n".join(lines)
    return text.strip()


